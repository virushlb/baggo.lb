function stockSized(s, m, l) {
  return { S: s, M: m, L: l };
}
function stockOne(n) {
  return { "One Size": n };
}

function localImage(id, variant) {
  const pid = String(id).padStart(3, "0");
  return `/products/p${pid}_${variant}.jpg`;
}

function productImages(id) {
  return [localImage(id, 1), localImage(id, 2), localImage(id, 3)];
}

